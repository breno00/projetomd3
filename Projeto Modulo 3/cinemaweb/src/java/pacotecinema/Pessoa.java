public class Pessoa {
    
    private String nome;
    private int idade;
    private int opiniao;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getOpiniao() {
        return opiniao;
    }

    public void setOpiniao(int opiniao) {
        this.opiniao = opiniao;
    }

}